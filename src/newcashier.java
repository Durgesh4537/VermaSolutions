    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String sql="insert into cashier (Username,Password,Name,Question,Answer) values (?,?,?,?,?)";
            try{
                pst=conn.prepareStatement(sql);
                pst.setString(1,username.getText());
                pst.setString(2,pass.getText());
                pst.setString(3,name.getText());
                pst.setString(4,(String) cmb2.getSelectedItem());
                pst.setString(5,seca.getText());
                 pst.execute();
                 
                JOptionPane.showMessageDialog(null, "Cashier Appointed");
         
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            finally{
                try{
                    rs.close();
                    pst.close();
                }catch (Exception e){
                    
                }
     }
            username.setText("");
        pass.setText("");
        name.setText("");
        seca.setText("");
         cmb2.setSelectedItem("SELECT YOUR SECURITY QUESTION");
      
    }//GEN-LAST:event_jButton1ActionPerformed

